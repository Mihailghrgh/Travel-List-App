import { useState } from 'react';
import { CLSThresholds } from 'web-vitals';
import { Logo } from './logo';
import { Form } from './form';
import { PackingList } from './PackingList';
import { Stats } from './Stats';

export default function App() {
  const [items, setItems] = useState([]);

  //NOT ALLOOWED TO MUTATE STATES
  function handleAddItems(item) {
    setItems((items) => [...items, item]);
  }

  function handlDeleteItem(id) {
    setItems((items) => items.filter((item) => item.id !== id));
  }

  //Looping through the array to find the correct ID through mapping
  function handleToggleItem(id) {
    setItems((items) =>
      items.map((item) =>
        item.id === id ? { ...item, packed: !item.packed } : item
      )
    );
  }

  function handleDeleteAllItems(item) {
    const confirmed = window.confirm(
      'Are you sure you want to clear your list ? '
    );

    if (confirmed) setItems([]);
  }

  return (
    <div className="app">
      <Logo />
      <Form onAddItems={handleAddItems} />
      <PackingList
        items={items}
        onDeleteItem={handlDeleteItem}
        onToggleItems={handleToggleItem}
        onDeleteAllItems={handleDeleteAllItems}
      />
      <Stats items={items} />
    </div>
  );
}









